// =====================================================================
// button_layout.cpp — собственно сама раскладка калькулятора.
// Один большой статический массив; читается ровно один раз
// при первом обращении.
// =====================================================================

#include "ui/button_layout.h"

namespace ui {

const std::vector<ButtonSpec>& button_specs() {
    // Локальные алиасы, чтобы инициализация массива читалась короче:
    // вместо ButtonKind::Digit писать K::Digit и т.д.
    using K = ButtonKind;
    using A = ButtonAction;

    // Раскладка 7×4 (7 строк, 4 колонки):
    //   row 0: C  ⌫  (  )
    //   row 1: ln exp sqrt abs
    //   row 2-4: цифры 1-9 + операторы / × −
    //   row 5: 0 (×2 колонки)  .  +
    //   row 6: =  (×4 колонки — на всю ширину)
    //
    // На кнопках нарисованы юникод-символы (÷, ×, −) для красоты.
    // В строку выражения они попадают через format::operator_to_internal,
    // который конвертирует их в ASCII (/ * -), понятные парсеру.
    //
    // static const — массив создаётся один раз при первом вызове и живёт
    // до конца программы. Возвращаем ссылку на него, чтобы не копировать.
    static const std::vector<ButtonSpec> specs = {
        // ---- Строка 0: служебные кнопки ----
        {"C",     0, 0, 1, 1, K::Action,   A::Clear},     // сброс всего
        {"⌫",    0, 1, 1, 1, K::Action,   A::Backspace}, // стереть последний символ
        {"(",     0, 2, 1, 1, K::Action,   A::Paren},     // открывающая скобка
        {")",     0, 3, 1, 1, K::Action,   A::Paren},     // закрывающая скобка

        // ---- Строка 1: функции варианта 2 ----
        {"ln",    1, 0, 1, 1, K::Function, A::Function},  // натуральный логарифм
        {"exp",   1, 1, 1, 1, K::Function, A::Function},  // экспонента
        {"sqrt",  1, 2, 1, 1, K::Function, A::Function},  // квадратный корень
        {"abs",   1, 3, 1, 1, K::Function, A::Function},  // модуль

        // ---- Строки 2-4: верхняя половина клавиатуры (как у обычного калькулятора) ----
        {"7",     2, 0, 1, 1, K::Digit,    A::Digit},
        {"8",     2, 1, 1, 1, K::Digit,    A::Digit},
        {"9",     2, 2, 1, 1, K::Digit,    A::Digit},
        {"÷",     2, 3, 1, 1, K::Operator, A::Operator},  // → / в строке выражения

        {"4",     3, 0, 1, 1, K::Digit,    A::Digit},
        {"5",     3, 1, 1, 1, K::Digit,    A::Digit},
        {"6",     3, 2, 1, 1, K::Digit,    A::Digit},
        {"×",     3, 3, 1, 1, K::Operator, A::Operator},  // → *

        {"1",     4, 0, 1, 1, K::Digit,    A::Digit},
        {"2",     4, 1, 1, 1, K::Digit,    A::Digit},
        {"3",     4, 2, 1, 1, K::Digit,    A::Digit},
        {"−",     4, 3, 1, 1, K::Operator, A::Operator},  // → -

        // ---- Строка 5: ноль на 2 колонки + точка + плюс ----
        {"0",     5, 0, 1, 2, K::Digit,    A::Digit},     // col_span=2
        {".",     5, 2, 1, 1, K::Digit,    A::Dot},       // десятичная точка
        {"+",     5, 3, 1, 1, K::Operator, A::Operator},

        // ---- Строка 6: равно на всю ширину ----
        {"=",     6, 0, 1, 4, K::Equals,   A::Equals},    // col_span=4
    };
    return specs;
}

}  // namespace ui
