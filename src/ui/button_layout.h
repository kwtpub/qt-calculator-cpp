// =====================================================================
// button_layout.h — описание раскладки кнопок калькулятора как ДАННЫХ.
//
// Идея: вынести "что есть кнопка / где она в сетке / что делает"
// из main_window.cpp в отдельный массив. Чтобы добавить, переместить
// или убрать кнопку, не нужно трогать UI-код — достаточно отредактировать
// массив в button_layout.cpp.
// =====================================================================

#pragma once

#include <QString>
#include <vector>

namespace ui {

// Стилевая категория кнопки. Используется QSS-селекторами в theme.cpp
// (через динамическое свойство kind="..."), чтобы покрасить кнопки
// разных типов в разные цвета.
enum class ButtonKind {
    Digit,      // цифры 0-9 (тёмно-серые)
    Operator,   // + - * / (жёлтые)
    Function,   // ln, exp, sqrt, abs (синие)
    Action,     // C, ⌫, (, ) (фиолетовые)
    Equals      // = (зелёная)
};

// Логическая роль кнопки — что делать при клике.
// Главное окно диспатчит обработку по этому полю в handle_action().
enum class ButtonAction {
    Digit,       // подставить цифру в строку выражения
    Operator,    // подставить +, -, *, / (с конвертацией ÷×− → /*-)
    Function,    // подставить "имя(" — сразу с открывающей скобкой
    Paren,       // подставить ( или )
    Dot,         // подставить . (десятичная точка)
    Clear,       // очистить дисплей и историю
    Backspace,   // стереть последний символ
    Equals       // вычислить выражение
};

// Полное описание одной кнопки.
struct ButtonSpec {
    QString label;          // что показывать на кнопке (видно пользователю)
    int row;                // строка в QGridLayout (0 — самая верхняя)
    int col;                // колонка в QGridLayout (0 — самая левая)
    int row_span;           // на сколько строк растянута (обычно 1)
    int col_span;           // на сколько колонок растянута ("0" и "=" — больше)
    ButtonKind kind;        // стилевая категория для QSS
    ButtonAction action;    // что делать при клике
};

// Полный список кнопок в порядке появления в сетке.
// Возвращает const-ссылку на статический массив — так что копий не делается.
const std::vector<ButtonSpec>& button_specs();

// Размеры сетки. constexpr — значения известны на этапе компиляции,
// можно использовать как размеры массивов и в for-циклах layout-растяжек.
constexpr int grid_rows = 7;
constexpr int grid_cols = 4;

}  // namespace ui
