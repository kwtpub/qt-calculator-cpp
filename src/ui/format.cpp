#include "ui/format.h"

// std::abs, std::isfinite для double.
#include <cmath>

namespace ui::format {

// Маппинг "красивых" операторов с кнопок в ASCII для парсера.
// Точное сравнение строк: возвращаем сразу как только нашли совпадение.
QString operator_to_internal(const QString& display) {
    if (display == "÷") return "/";
    if (display == "×") return "*";
    if (display == "−") return "-";   // это U+2212, не дефис
    return display;                    // обычный символ — отдаём как есть
}

// Форматирует число для дисплея.
//
// Правила:
//   1. Целое (без дробной части) — без точки: "5", "-42".
//   2. Дробное в "человеческом" диапазоне — обычная запись с убранными
//      хвостовыми нулями: "3.75", "0.1", "2.5", "0.00001".
//   3. Очень большие/маленькие — научная запись: "1.23e+20".
QString format_result(double r) {
    // Целое и в безопасном диапазоне для long long — печатаем без точки.
    if (r == static_cast<long long>(r) && std::abs(r) < 1e15) {
        return QString::number(static_cast<long long>(r));
    }

    const double abs_r = std::abs(r);

    // "Человеческий" диапазон — от 1e-4 до 1e15. Используем формат 'f'
    // (фиксированная запись) с большой точностью, потом убираем хвостовые нули.
    // Это даёт "0.00001" вместо "1e-05" и "3.75" вместо "3.75000000".
    if (abs_r >= 1e-4 && abs_r < 1e15) {
        QString s = QString::number(r, 'f', 12);   // 12 знаков после точки
        // Убираем хвостовые нули, потом — лишнюю точку (если все нули были).
        while (s.endsWith('0')) s.chop(1);
        if (s.endsWith('.')) s.chop(1);
        return s;
    }

    // Очень большие/маленькие числа — научная запись с 12 значащими цифрами.
    return QString::number(r, 'g', 12);
}

}  // namespace ui::format
