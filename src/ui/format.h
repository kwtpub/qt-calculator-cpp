// =====================================================================
// format.h — утилиты форматирования для GUI.
//
// Делает две вещи:
//   1) Конвертирует "красивые" символы операторов с кнопок (÷ × −)
//      в ASCII (/, *, -), понятные парсеру.
//   2) Печатает результат вычисления так, чтобы это было приятно читать.
// =====================================================================

#pragma once

#include <QString>

namespace ui::format {

// Преобразование одного оператора, как нарисован на кнопке, в ASCII.
// ÷ → /, × → *, − → - (это юникодный минус U+2212, не дефис).
// Для всех остальных символов возвращает строку как есть.
QString operator_to_internal(const QString& display);

// Превращает результат типа double в строку для дисплея.
// Целые числа печатаем без точки ("5" а не "5.000000"),
// дробные — с точностью до 12 значащих цифр (формат 'g' в Qt).
QString format_result(double r);

}  // namespace ui::format
