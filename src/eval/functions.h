// =====================================================================
// functions.h — математические функции варианта 2: ln, exp, sqrt, abs.
//
// Каждая функция обёрнута отдельно (apply_ln, apply_exp, ...) — так их
// проще тестировать и читать сообщения об ошибках. Над ними стоит
// общий диспетчер apply(), который вызывают из evaluator'а.
// =====================================================================

#pragma once

#include <string>

namespace eval::functions {

// Натуральный логарифм. Требует x > 0; иначе EvalError.
double apply_ln(double x);

// Экспонента e^x. Определена для любого x; при больших x может выйти
// за пределы double — это поймает check_finite в evaluator'е.
double apply_exp(double x);

// Квадратный корень. Требует x >= 0; иначе EvalError.
double apply_sqrt(double x);

// Модуль (абсолютное значение). Определён для любого x.
double apply_abs(double x);

// Диспетчер по имени функции. Принимает строку (как в Token::text)
// и аргумент. Бросает EvalError для неизвестного имени —
// сюда мы попадаем только если токенизатор пропустил что-то лишнее
// (этого не должно случаться, но защита от багов программы).
double apply(const std::string& name, double x);

}  // namespace eval::functions
