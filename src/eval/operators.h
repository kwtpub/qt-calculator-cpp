// =====================================================================
// operators.h — применение арифметических операторов.
//
// Этот модуль изолирован от evaluator'а специально: чтобы добавить
// новый оператор (например, %), достаточно поправить operators.cpp,
// не трогая основной цикл вычисления.
// =====================================================================

#pragma once

#include <string>

namespace eval::operators {

// Применяет один из бинарных операторов: +, -, *, /.
// op — текстовое имя оператора (как в Token::text).
// a — левый операнд, b — правый. Порядок ВАЖЕН для - и /.
// Бросает EvalError при делении на ноль или неизвестном операторе.
double apply_binary(const std::string& op, double a, double b);

// Применяет унарный оператор. На текущий момент поддерживается
// только "u-" (унарный минус). Других унарных у нас нет.
// Бросает EvalError при неизвестном операторе.
double apply_unary(const std::string& op, double a);

}  // namespace eval::operators
