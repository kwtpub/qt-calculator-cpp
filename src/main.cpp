// =====================================================================
// main.cpp — точка входа в программу.
// Создаёт Qt-приложение, главное окно и запускает цикл событий Qt.
// =====================================================================

// Подключаем заголовок главного окна — класс ui::MainWindow.
// Он умеет создавать всю иерархию виджетов (дисплей + кнопки) и
// связывать клики/клавиатуру с парсером и вычислителем.
#include "ui/main_window.h"

// QApplication — основной класс Qt-приложения. Он управляет циклом
// событий (events), глобальными настройками шрифта/стиля, обрабатывает
// аргументы командной строки и т.п. Без него ни один Qt-виджет работать
// не будет.
#include <QApplication>

// Стандартная сигнатура main для Qt: argc/argv обязательны, потому что
// QApplication читает из них флаги вида --style, --platform и др.
int main(int argc, char* argv[]) {
    // Создаём объект приложения. После этой строки Qt инициализирован:
    // подгружены плагины платформы (cocoa на macOS), создан главный
    // event-loop (пока не запущен), настроены шрифты по умолчанию.
    QApplication app(argc, argv);

    // Имя приложения — используется в заголовке окон по умолчанию,
    // в QSettings (настройках), в системных диалогах macOS и т.д.
    app.setApplicationName("Calculator");

    // Имя организации — нужно паре с applicationName для путей QSettings
    // (например, ~/Library/Preferences/com.M3O-103BV-25.Calculator.plist).
    app.setOrganizationName("M3O-103BV-25");

    // Создаём главное окно на стеке. Конструктор MainWindow строит
    // всю UI-иерархию: дисплей с историей/полем ввода/результатом и
    // сетку кнопок 7×4.
    ui::MainWindow w;

    // Делаем окно видимым. До вызова show() окно создано в памяти,
    // но на экране его нет.
    w.show();

    // Запускаем цикл событий. Управление возвращается в main только
    // когда последнее окно закрыто (или вызван QApplication::quit()).
    // Возвращаемое значение — код выхода: 0 = нормальное завершение.
    return app.exec();
}
