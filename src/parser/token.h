// =====================================================================
// token.h — описание ТОКЕНА: атомарной единицы выражения.
// Токены — это то, что выдаёт токенизатор и что потом перерабатывает
// shunting_yard в обратную польскую запись.
// =====================================================================

// pragma once — современная замена include guard'ов (#ifndef ... #define).
// Гарантирует, что заголовок включится в каждую единицу трансляции
// ровно один раз. Поддерживается всеми актуальными компиляторами.
#pragma once

// Нужен std::string для поля text (исходный текст токена).
#include <string>

// Все типы парсера живут в namespace parser, чтобы не конфликтовать
// с одноимёнными сущностями из других модулей или Qt.
namespace parser {

// Перечисление типов токена. enum class (а не просто enum) даёт
// строгую типизацию: TokenType::Number нельзя случайно сравнить
// с числом, и значения не утекают в окружающую область видимости.
enum class TokenType {
    Number,       // числовой литерал: 2, 3.14, .5, 100
    Operator,     // бинарный оператор +, -, *, / либо унарный u-
                  // (u- — внутреннее обозначение унарного минуса)
    Function,     // имя функции варианта 2: ln, exp, sqrt, abs
    LeftParen,    // открывающая скобка '('
    RightParen    // закрывающая скобка ')'
};

// Структура одного токена. Хранит тип, исходный текст и числовое
// значение (только для TokenType::Number; в остальных случаях
// поле value просто остаётся равно 0.0 и не используется).
struct Token {
    // Тип токена — по нему shunting_yard и evaluator выбирают ветку.
    TokenType type;

    // Исходный текст: "+", "sqrt", "3.14", "(" и т.п.
    // Для Operator и Function он используется как ключ при диспетчеризации.
    std::string text;

    // Числовое значение. Заполняется только для Number в read_number.
    // Инициализация по умолчанию = 0.0, чтобы не было неинициализированной памяти.
    double value = 0.0;
};

}  // namespace parser
