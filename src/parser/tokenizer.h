// =====================================================================
// tokenizer.h — лексический анализатор (токенизатор).
//
// Превращает плоскую строку выражения в массив структурированных
// токенов. Сразу отлавливает базовые синтаксические ошибки.
//
// Пример работы:
//   "2+3*sqrt(16)"
//      ↓ tokenize
//   [Number(2), Op(+), Number(3), Op(*), Function(sqrt), '(', Number(16), ')']
// =====================================================================

#pragma once

// Токены — наш собственный тип, нужен для возвращаемого значения.
#include "parser/token.h"

// std::string — входная строка; std::vector — выходной массив.
#include <string>
#include <vector>

// Вкладываем токенизатор внутрь parser в подпространство tokenizer.
// Так вызов выглядит как parser::tokenizer::tokenize(...) — самодокументирующийся.
namespace parser::tokenizer {

// Проверяет, входит ли имя в список поддерживаемых функций.
// Текущий вариант 2: ln, exp, sqrt, abs.
// Если в варианте появится новая функция, достаточно добавить её
// в множество в реализации — больше менять нигде не нужно.
bool is_known_function(const std::string& name);

// Главная функция модуля. На входе — выражение от пользователя.
// На выходе — массив токенов в том же порядке.
//
// Бросает ParseError при:
//  - двух точках в одном числе ("3.14.5"),
//  - неизвестной функции ("foo(2)"),
//  - неизвестном символе ("2 & 3"),
//  - двух операторах подряд ("2++3"),
//  - пустой строке после удаления пробелов.
std::vector<Token> tokenize(const std::string& expression);

}  // namespace parser::tokenizer
