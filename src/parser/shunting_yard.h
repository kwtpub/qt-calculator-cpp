// =====================================================================
// shunting_yard.h — алгоритм Дейкстры (Shunting-yard).
//
// Преобразует массив токенов из инфиксной формы (как пишут люди)
//     2 + 3 * 4
// в обратную польскую запись (RPN, постфиксная):
//     2 3 4 * +
//
// RPN не нуждается в скобках и приоритетах — её можно посчитать
// одним проходом по стеку. Этим занимается evaluator.
// =====================================================================

#pragma once

#include "parser/token.h"

#include <string>
#include <vector>

namespace parser::shunting_yard {

// Возвращает приоритет оператора. Чем больше число, тем сильнее
// связывает оператор. Используется при решении "что вытолкнуть из стека".
//
// Текущие приоритеты:
//   u- (унарный минус) = 4
//   *, /              = 3
//   +, -              = 2
//   (скобки и прочее) = 0
int precedence(const std::string& op);

// Возвращает true для операторов с правой ассоциативностью.
// У нас такой только унарный минус: --5 = -(-5), а не (-)(-5).
// Все бинарные (+, -, *, /) — левоассоциативные.
bool is_right_associative(const std::string& op);

// Главная функция модуля. На входе — токены инфиксной формы,
// на выходе — токены в обратной польской записи.
//
// Бросает ParseError при несбалансированных скобках:
//  - "(1+2"  → лишняя '(' (не нашли парную ')')
//  - "1+2)"  → лишняя ')' (не нашли парную '(')
std::vector<Token> to_rpn(const std::vector<Token>& tokens);

}  // namespace parser::shunting_yard
