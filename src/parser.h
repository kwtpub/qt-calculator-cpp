// =====================================================================
// parser.h — публичный интерфейс калькулятора.
//
// Один класс Parser реализует и разбор, и вычисление выражения через
// рекурсивный спуск. Грамматика:
//
//   expression := term   (('+' | '-') term)*
//   term       := factor (('*' | '/') factor)*
//   factor     := number
//               | '(' expression ')'
//               | ('+' | '-') factor      // унарный минус/плюс
//               | name '(' expression ')' // ln, exp, sqrt, abs
//
// Идея рекурсивного спуска: приоритеты операторов выражены ПОРЯДКОМ
// вызова функций. parseExpression вызывает parseTerm — поэтому * и /
// связывают сильнее, чем + и -.
//
// Все ошибки (синтаксис и математика) — std::runtime_error.
// =====================================================================

#pragma once

#include <stdexcept>
#include <string>

namespace calc {

// Главная функция: строка → результат.
// Бросает std::runtime_error при любой ошибке (пустая строка,
// неизвестный символ, деление на ноль, ln/sqrt вне области и т.п.).
double evaluate(const std::string& expression);

}  // namespace calc
